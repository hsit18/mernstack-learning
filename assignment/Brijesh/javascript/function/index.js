function add(x,y){
    let result= x+y;
    console.log(result);
}


function multiply(x,y){
    let result= x*y;
    console.log(result);
}

function sub(x,y){
    let result= x-y;
    console.log(result);
}

function divide(x,y){
    let result= x/y;
    console.log(result);
}

function rem(x,y){
    let result=x%y;
    console.log(result)
}

// arrow function

const addition = (x,y) => x+y;

const subtraction = (x,y) => x-y;

const multipication = (x,y) => x*y;

const division = (x,y) => x/y;

const remainder = (x,y) => x%y;
